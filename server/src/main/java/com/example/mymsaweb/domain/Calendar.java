package com.example.mymsaweb.domain;


public class Calendar {
  private long month;
  private long day;

  public long getMonth() {
    return month;
  }

  public void setMonth(long month) {
    this.month = month;
  }

  public long getDay() {
    return day;
  }

  public void setDay(long day) {
    this.day = day;
  }
}
